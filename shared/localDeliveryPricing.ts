/**
 * Get delivery fee for a zip code
 * Returns the fee if zip code is in the pricing table, null otherwise
 */
export function getDeliveryFee(zipCode: string): number | null {
  const pricing = LOCAL_DELIVERY_ZIP_CODES.find(z => z.zipCode === zipCode);
  return pricing ? pricing.deliveryFee : null;
}

/**
 * Check if a zip code is eligible for local delivery
 */
export function isLocalDeliveryAvailable(zipCode: string): boolean {
  return LOCAL_DELIVERY_ZIP_CODES.some(z => z.zipCode === zipCode);
}
